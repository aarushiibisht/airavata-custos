package org.apache.custos.tenant.service.api.controllers;

import org.apache.custos.tenant.service.model.Tenant;
import org.springframework.http.ResponseEntity;
import org.springframework.validation.annotation.Validated;
import org.springframework.web.bind.annotation.PathVariable;
import org.springframework.web.bind.annotation.RequestBody;
import org.springframework.web.bind.annotation.RequestHeader;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RequestMethod;
import org.springframework.web.bind.annotation.RequestParam;
import org.springframework.web.bind.annotation.RequestPart;
import org.springframework.web.multipart.MultipartFile;

import javax.validation.Valid;
import javax.validation.constraints.*;
import java.util.List;

public interface TenantsApi {


    @RequestMapping(value = "/tenants",
        produces = { "application/json" }, 
        method = RequestMethod.GET)
    ResponseEntity<List<Tenant>> getTenants();


    @RequestMapping(value = "/tenants/status/{approvalStatus}",
        produces = { "application/json" }, 
        method = RequestMethod.GET)
    ResponseEntity<List<Tenant>> getTenantsInStatus(@PathVariable("approvalStatus") String approvalStatus);

}
